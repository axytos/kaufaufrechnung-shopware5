    public const PAYMENT_STATE_2ND_REMINDER = 'PAYMENT_STATE_2ND_REMINDER';
    public const PAYMENT_STATE_3RD_REMINDER = 'PAYMENT_STATE_3RD_REMINDER';
    public const PAYMENT_STATE_ENCASHMENT = 'PAYMENT_STATE_ENCASHMENT';
    public const PAYMENT_STATE_OPEN = 'PAYMENT_STATE_OPEN';
