        self::PAYMENT_STATE_PARTIALLY_INVOICED => Status::PAYMENT_STATE_PARTIALLY_INVOICED,
        self::PAYMENT_STATE_COMPLETELY_INVOICED => Status::PAYMENT_STATE_COMPLETELY_INVOICED,
